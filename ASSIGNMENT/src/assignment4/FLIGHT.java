package assignment4;

public class FLIGHT {

	String FlightId;
	String DepartureFrom;
	String DepartureTo;
	double Duration;
	boolean MealAvailable;
	public FLIGHT(String flightId, String departureFrom, String departureTo, double duration, boolean mealAvailable) {
		super();
		FlightId = flightId;
		DepartureFrom = departureFrom;
		DepartureTo = departureTo;
		Duration = duration;
		MealAvailable = mealAvailable;
	}
	
	
}
